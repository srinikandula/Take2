public class SplitString {
    public static void main(String[] args) {

        String dev = "How are you doing";
        System.out.println(dev.substring(0,3));
        System.out.println(dev.substring(4,7));
        System.out.println(dev.substring(8,11));
        System.out.println(dev.substring(12,17));


    }
}
